#!/usr/bin/env node
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, basename } from 'node:path';

const DIR = new URL('../directories/', import.meta.url).pathname;
const OUT = new URL('../migrations/0002_sync_directories.sql', import.meta.url).pathname;

const ORIGINAL_NAME_BY_SLUG = {
  'indie-hackers': 'Indie Hackers',
  'devhunt': 'DevHunt',
  'theres-an-ai-for-that': "There's an AI for That",
  'futurepedia': 'Futurepedia',
  'uneed': 'Uneed',
  'openalternative': 'OpenAlternative',
  'peerlist': 'Peerlist',
  'launching-next': 'Launching Next',
  'microlaunch': 'Microlaunch',
  'goodfirms': 'Goodfirms',
  'tekpon': 'Tekpon',
  'appsumo': 'AppSumo',
  'stacker-news': 'Stacker News',
  'software-advice': 'Software Advice',
  'fazier': 'Fazier',
};

const sqlEscape = (v) => {
  if (v === null || v === undefined || v === '') return 'NULL';
  if (typeof v === 'number') return String(v);
  return `'${String(v).replace(/'/g, "''")}'`;
};

const parseMeta = (md) => {
  const meta = {};
  const lines = md.split('\n');
  for (const line of lines) {
    const m = line.match(/^- \*\*([^:]+):\*\*\s*(.*)$/);
    if (m) meta[m[1].trim()] = m[2].trim();
  }
  return meta;
};

const parseFields = (md) => {
  const fieldsSection = md.split('## Fields')[1]?.split('## Gotchas')[0] ?? '';
  const lines = fieldsSection.split('\n').filter(l => l.startsWith('|'));
  if (lines.length < 2) return [];
  // skip header + separator
  const dataLines = lines.slice(2);
  const fields = [];
  let order = 0;
  for (const line of dataLines) {
    const cells = line.split('|').slice(1, -1).map(c => c.trim());
    if (cells.length < 7) continue;
    const [field_name, field_label, required, max_length, dropdown_values, selector, notes] = cells;
    // skip placeholder/em-dash rows
    if (!field_name || field_name === '—' || field_name === '-') continue;
    if (field_name === 'field_name') continue;
    fields.push({
      field_name,
      field_label: field_label === '—' ? null : field_label,
      is_required: /^yes/i.test(required) ? 1 : 0,
      max_length: /^\d+$/.test(max_length) ? parseInt(max_length, 10) : null,
      dropdown_values: (dropdown_values && dropdown_values !== '—') ? dropdown_values : null,
      selector: (selector && selector !== '—') ? selector : null,
      notes: (notes && notes !== '—') ? notes : null,
      field_order: order++,
    });
  }
  return fields;
};

const extractEase = (str) => {
  if (!str) return null;
  if (str.includes('🟢')) return 'easy';
  if (str.includes('🟡')) return 'medium';
  if (str.includes('🔴')) return 'hard';
  return null;
};

const extractTier = (auth, free) => {
  if (/sales/i.test(auth)) return 'sales-led';
  if (/^no/i.test(free)) return 'paid-only';
  if (/^none/i.test(auth) || /^email/i.test(auth)) return 'free + open';
  return 'free + gated';
};

const recipes = [];
for (const file of readdirSync(DIR)) {
  if (!file.endsWith('.md')) continue;
  if (file === 'requirements-matrix.md' || file === '_template.md') continue;
  const slug = file.replace(/\.md$/, '');
  const md = readFileSync(join(DIR, file), 'utf8');
  const titleMatch = md.match(/^#\s+(.+)$/m);
  const name = ORIGINAL_NAME_BY_SLUG[slug] ?? titleMatch?.[1].trim() ?? slug;
  const meta = parseMeta(md);
  const fields = parseFields(md);
  recipes.push({
    slug,
    name,
    url: (meta['URL'] ?? '').replace(/\s+.*$/, ''),
    submit_url: (meta['Submit URL'] ?? '').replace(/\s+\(.*$/, ''),
    auth: meta['Auth'] ?? '',
    has_captcha: /^yes/i.test(meta['Captcha'] ?? '') ? 1 : 0,
    approval: meta['Approval'] ?? '',
    is_free: /^no/i.test(meta['Free'] ?? '') ? 0 : 1,
    one_line: meta['One-line'] ?? '',
    ease: extractEase(meta['Ease']),
    ease_reason: meta['Ease reason'] ?? null,
    fields,
  });
}

console.log(`Parsed ${recipes.length} recipes`);
console.log(`  ${recipes.filter(r => ORIGINAL_NAME_BY_SLUG[r.slug]).length} existing D1 dirs to UPDATE`);
console.log(`  ${recipes.filter(r => !ORIGINAL_NAME_BY_SLUG[r.slug]).length} new dirs to INSERT`);
console.log(`  ${recipes.reduce((s, r) => s + r.fields.length, 0)} total field rows`);

const sql = [];
sql.push('-- 0002_sync_directories.sql');
sql.push('-- Generated by scripts/sync-directories.mjs from per-directory recipes');
sql.push('');
sql.push('-- Schema additions (idempotent via try/catch in caller)');
sql.push('ALTER TABLE directories ADD COLUMN slug TEXT;');
sql.push('ALTER TABLE directories ADD COLUMN ease TEXT;');
sql.push('ALTER TABLE directories ADD COLUMN ease_reason TEXT;');
sql.push('ALTER TABLE directory_fields ADD COLUMN selector TEXT;');
sql.push('ALTER TABLE directory_fields ADD COLUMN dropdown_values TEXT;');
sql.push('ALTER TABLE directory_fields ADD COLUMN field_order INTEGER DEFAULT 0;');
sql.push('CREATE UNIQUE INDEX IF NOT EXISTS directories_slug_idx ON directories(slug);');
sql.push('');

sql.push('-- Update existing 20 directories with slug + ease + ease_reason');
const existingSlugByName = {
  'Product Hunt': 'product-hunt',
  'Hacker News': 'hacker-news',
  'BetaList': 'betalist',
  'Indie Hackers': 'indie-hackers',
  'AlternativeTo': 'alternativeto',
  'SaaSHub': 'saashub',
  'DevHunt': 'devhunt',
  "There's an AI for That": 'theres-an-ai-for-that',
  'Futurepedia': 'futurepedia',
  'Uneed': 'uneed',
  'OpenAlternative': 'openalternative',
  'Peerlist': 'peerlist',
  'Launching Next': 'launching-next',
  'Microlaunch': 'microlaunch',
  'Goodfirms': 'goodfirms',
  'Tekpon': 'tekpon',
  'AppSumo': 'appsumo',
  'Stacker News': 'stacker-news',
  'Software Advice': 'software-advice',
  'Fazier': 'fazier',
};
// Backfill slug for all 20 existing
const EXISTING_EASE = {
  'Product Hunt': ['easy', 'hero target, mapped'],
  'Hacker News': ['easy', 'free public submit, simple format'],
  'BetaList': ['medium', 'mapped but 60-day queue'],
  'Indie Hackers': ['medium', 'SPA — needs logged-in first-run capture'],
  'AlternativeTo': ['easy', 'free + account, manual ~3d'],
  'SaaSHub': ['easy', 'free + account, manual ~2d'],
  'DevHunt': ['hard', '$49 wall on every launch week for next 13mo; one free slot exists ~June 2027'],
  "There's an AI for That": ['hard', '$49 min · Cloudflare · multi-step funnel'],
  'Futurepedia': ['hard', '$247 basic · $497 verified · no free path'],
  'Uneed': ['medium', 'gated · slow free queue · also a competitor'],
  'OpenAlternative': ['medium', 'OSS-only · github_url required'],
  'Peerlist': ['medium', 'Mondays only · individuals only · no upvote asks'],
  'Launching Next': ['easy', 'publicly mapped · math captcha trivial'],
  'Microlaunch': ['hard', 'fully gated · zero public visibility'],
  'Goodfirms': ['hard', 'Cloudflare blocks · B2B address/phone fields'],
  'Tekpon': ['hard', '$249 one-time · 5-step wizard · 300-word cap'],
  'AppSumo': ['hard', '~10% accept · quarterly · live test creds'],
  'Stacker News': ['medium', 'needs sats wallet · territory choice matters'],
  'Software Advice': ['hard', 'sales onboarding, not a form to fill'],
  'Fazier': ['medium', 'free tier needs reciprocal backlink'],
};
for (const [name, slug] of Object.entries(existingSlugByName)) {
  const [ease, easeReason] = EXISTING_EASE[name];
  sql.push(`UPDATE directories SET slug=${sqlEscape(slug)}, ease=${sqlEscape(ease)}, ease_reason=${sqlEscape(easeReason)} WHERE name=${sqlEscape(name)};`);
}
sql.push('');

sql.push('-- Insert 10 new directories');
for (const r of recipes) {
  if (ORIGINAL_NAME_BY_SLUG[r.slug]) continue; // skip already-seeded
  sql.push(`INSERT OR IGNORE INTO directories (name, slug, url, submit_url, description, auth_type, has_captcha, is_free, ease, ease_reason, status, priority) VALUES (${sqlEscape(r.name)}, ${sqlEscape(r.slug)}, ${sqlEscape(r.url)}, ${sqlEscape(r.submit_url)}, ${sqlEscape(r.one_line)}, ${sqlEscape(r.auth)}, ${r.has_captcha}, ${r.is_free}, ${sqlEscape(r.ease)}, ${sqlEscape(r.ease_reason)}, 'active', 40);`);
}
sql.push('');

sql.push('-- Update auth_type + has_captcha + is_free + submit_url on existing dirs to reflect recipe-derived values');
for (const r of recipes) {
  if (!ORIGINAL_NAME_BY_SLUG[r.slug]) continue;
  sql.push(`UPDATE directories SET auth_type=${sqlEscape(r.auth)}, has_captcha=${r.has_captcha}, is_free=${r.is_free}, submit_url=${sqlEscape(r.submit_url)} WHERE slug=${sqlEscape(r.slug)};`);
}
sql.push('');

sql.push('-- Clear and reseed directory_fields for the 25 recipe dirs (preserves the original 5)');
const recipeSlugList = recipes.map(r => `'${r.slug}'`).join(', ');
sql.push(`DELETE FROM directory_fields WHERE directory_id IN (SELECT id FROM directories WHERE slug IN (${recipeSlugList}));`);
sql.push('');

sql.push('-- Insert field rows from recipes');
for (const r of recipes) {
  for (const f of r.fields) {
    sql.push(
      `INSERT INTO directory_fields (directory_id, field_name, field_label, is_required, max_length, dropdown_values, selector, notes, field_order) ` +
      `SELECT id, ${sqlEscape(f.field_name)}, ${sqlEscape(f.field_label)}, ${f.is_required}, ${f.max_length === null ? 'NULL' : f.max_length}, ${sqlEscape(f.dropdown_values)}, ${sqlEscape(f.selector)}, ${sqlEscape(f.notes)}, ${f.field_order} FROM directories WHERE slug=${sqlEscape(r.slug)};`
    );
  }
}

writeFileSync(OUT, sql.join('\n') + '\n');
console.log(`\nWrote ${sql.length} SQL lines to ${OUT}`);
